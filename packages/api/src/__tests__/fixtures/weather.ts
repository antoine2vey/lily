const oneDayMs = 24 * 60 * 60 * 1000

export const makeSchedulePlant = (
  overrides: Partial<PlantForScheduleDelta> & {
    wateringFrequencyDays: number
    isOutdoor: boolean
  }
): PlantForScheduleDelta => {
  const now = Date.now()
  const freq = overrides.wateringFrequencyDays
  return {
    id: 'test-plant',
    category: 'Foliage',
    wateringRating: 3,
    lastWateredAt: new Date(now - 3 * oneDayMs),
    nextWateringAt: new Date(now + (freq - 3) * oneDayMs),
    nextFertilizationAt: null,
    fertilizationFrequencyDays: null,
    ...overrides,
  }
}

